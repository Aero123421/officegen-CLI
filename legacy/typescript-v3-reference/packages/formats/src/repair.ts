import { edit, type EditOperation } from "./edit.js";
import { diagnose, type DiagnoseIssue, type DiagnoseResult } from "./diagnose.js";
import { type InputLike, type OfficegenConfig, normalizeInput, sha256 } from "./shared.js";

export interface RepairOptions {
  out?: string;
  dryRun?: boolean;
  issues?: DiagnoseIssue[] | DiagnoseResult;
  config?: OfficegenConfig;
}

export type RepairTaxonomyCategory = "quality" | "compatibility" | "security" | "environment";
export type RepairTaxonomySeverity = "info" | "warning" | "error" | "critical";

export interface RepairFailureTaxonomyEntry {
  code: string;
  category: RepairTaxonomyCategory;
  severity: RepairTaxonomySeverity;
  autoRepairable: boolean;
  evidence: Array<{ kind: string; message: string; stableObjectId?: string; issueCode?: string }>;
  nextCommand: string;
}

export interface RepairPlanV2 {
  schema: "officegen.repairPlan@2";
  version: 2;
  target: string;
  input?: string;
  inputSha256: string;
  wouldWrite: boolean;
  planOnly: boolean;
  operations: EditOperation[];
  failureTaxonomy: RepairFailureTaxonomyEntry[];
  steps: Array<{ id: string; command: string; dryRun: boolean; reason: string }>;
  verify: {
    status: "not_run";
    requiredAfterRepair: boolean;
    command: string;
    readinessNote: string;
  };
}

export interface RepairResult {
  schema: "officegen.repair.result@1.2";
  format: string;
  inputSha256: string;
  applied: number;
  changed: boolean;
  out?: string;
  suggestedOps: EditOperation[];
  failureTaxonomy: RepairFailureTaxonomyEntry[];
  repairPlan: RepairPlanV2;
  readiness?: "warning";
  readinessNotes?: string[];
  postRepairVerify: RepairPlanV2["verify"];
  caveats: string[];
}

export async function repair(input: InputLike, options: RepairOptions = {}): Promise<RepairResult> {
  const normalized = await normalizeInput(input, "unknown");
  const issueList = Array.isArray(options.issues)
    ? options.issues
    : options.issues?.schema === "officegen.diagnose.result@1.2"
      ? options.issues.issues
      : (await diagnose({ data: normalized.bytes, path: normalized.path, format: normalized.format }, { config: options.config })).issues;
  const suggestedOps = normalizeSuggestedOps(issueList.flatMap((issue) => issue.suggestedOps ?? []));
  const failureTaxonomy = buildFailureTaxonomy(issueList, suggestedOps, normalized.format);
  const inputSha256 = `sha256:${sha256(normalized.bytes)}`;
  const repairPlan = buildRepairPlan({
    inputPath: normalized.path,
    inputSha256,
    format: normalized.format,
    suggestedOps,
    failureTaxonomy,
    out: options.out,
    planOnly: true,
    wouldWrite: false
  });
  const postRepairVerify = repairPlan.verify;
  if (!suggestedOps.length || options.dryRun) {
    return {
      schema: "officegen.repair.result@1.2",
      format: normalized.format,
      inputSha256,
      applied: 0,
      changed: false,
      out: undefined,
      suggestedOps,
      failureTaxonomy,
      repairPlan,
      postRepairVerify,
      caveats: ["No automatically safe repair operations were available; suggestedOps can be reviewed by an agent or user."]
    };
  }
  const edited = await edit({ data: normalized.bytes, path: normalized.path, format: normalized.format }, suggestedOps, { out: options.out, config: options.config });
  const readinessNotes = edited.out
    ? ["Repair wrote an output artifact, but post-repair verify has not been run."]
    : undefined;
  return {
    schema: "officegen.repair.result@1.2",
    format: normalized.format,
    inputSha256,
    applied: edited.applied,
    changed: edited.changed,
    out: edited.out,
    suggestedOps,
    failureTaxonomy,
    repairPlan: buildRepairPlan({
      inputPath: normalized.path,
      inputSha256,
      format: normalized.format,
      suggestedOps,
      failureTaxonomy,
      out: edited.out ?? options.out,
      planOnly: false,
      wouldWrite: Boolean(edited.out)
    }),
    readiness: readinessNotes ? "warning" : undefined,
    readinessNotes,
    postRepairVerify: buildPostRepairVerify(normalized.format, edited.out ?? options.out),
    caveats: ["Repair applies only conservative edit operations generated by diagnose."]
  };
}

export const repairDocument = repair;

function buildRepairPlan(context: {
  inputPath?: string;
  inputSha256: string;
  format: string;
  suggestedOps: EditOperation[];
  failureTaxonomy: RepairFailureTaxonomyEntry[];
  out?: string;
  planOnly: boolean;
  wouldWrite: boolean;
}): RepairPlanV2 {
  return {
    schema: "officegen.repairPlan@2",
    version: 2,
    target: context.format,
    input: context.inputPath,
    inputSha256: context.inputSha256,
    wouldWrite: context.wouldWrite,
    planOnly: context.planOnly,
    operations: context.suggestedOps,
    failureTaxonomy: context.failureTaxonomy,
    steps: [
      {
        id: "repair-dry-run",
        command: `officegen repair <input.${context.format}> --dry-run --json`,
        dryRun: true,
        reason: "Preview conservative repair operations before writing an Office artifact."
      },
      {
        id: "post-repair-verify",
        command: buildVerifyCommand(context.format, context.out),
        dryRun: true,
        reason: "Run after any repair output is written to prove openability and repair-dialog readiness."
      }
    ],
    verify: buildPostRepairVerify(context.format, context.out)
  };
}

function buildPostRepairVerify(format: string, out?: string): RepairPlanV2["verify"] {
  return {
    status: "not_run",
    requiredAfterRepair: true,
    command: buildVerifyCommand(format, out),
    readinessNote: "Post-repair verify has not been run; do not treat the repaired artifact as release-ready until verify succeeds."
  };
}

function normalizeSuggestedOps(values: unknown[]): EditOperation[] {
  return values
    .map((value) => {
      if (!value || typeof value !== "object" || Array.isArray(value)) return undefined;
      const record = { ...(value as Record<string, unknown>) };
      if (typeof record.op !== "string" && typeof record.type === "string") {
        record.op = record.type;
        delete record.type;
      }
      return record as EditOperation;
    })
    .filter((value): value is EditOperation => Boolean(value));
}

function buildFailureTaxonomy(issues: DiagnoseIssue[], suggestedOps: EditOperation[], format: string): RepairFailureTaxonomyEntry[] {
  const entries: RepairFailureTaxonomyEntry[] = issues.map((issue) => ({
    code: issue.code,
    category: taxonomyCategory(issue.code),
    severity: taxonomySeverity(issue.severity),
    autoRepairable: Boolean(issue.suggestedOps?.length),
    evidence: [{
      kind: "diagnose-issue",
      issueCode: issue.code,
      message: issue.message,
      stableObjectId: issue.stableObjectId
    }],
    nextCommand: nextCommandForIssue(issue, format)
  }));
  if (!suggestedOps.length) {
    entries.push({
      code: "REPAIR_NO_SAFE_OPS",
      category: "compatibility",
      severity: issues.some((issue) => issue.severity === "error") ? "error" : "warning",
      autoRepairable: false,
      evidence: [{
        kind: "repair-analysis",
        issueCode: "REPAIR_NO_SAFE_OPS",
        message: "No conservative EditOps were generated from the diagnosed issues.",
        stableObjectId: undefined
      }],
      nextCommand: `officegen diagnose <input.${format}> --json`
    });
  }
  return entries;
}

function taxonomyCategory(code: string): RepairTaxonomyCategory {
  if (code.startsWith("MACRO") || code.startsWith("SECURITY_")) return "security";
  if (code.includes("REPAIR") || code.includes("RELATIONSHIP") || code.includes("MISSING_PART")) return "compatibility";
  if (code.includes("NATIVE") || code.includes("RENDERER")) return "environment";
  return "quality";
}

function taxonomySeverity(severity: DiagnoseIssue["severity"]): RepairTaxonomySeverity {
  return severity;
}

function nextCommandForIssue(issue: DiagnoseIssue, format: string): string {
  if (issue.suggestedOps?.length) return `officegen repair <input.${format}> --dry-run --json`;
  if (issue.code.startsWith("OFFICE_REPAIR_RISK")) return `officegen verify <input.${format}> --native --visual --json`;
  if (issue.code === "MACRO_PRESENT") return `officegen inspect <input.${format}> --json`;
  return `officegen diagnose <input.${format}> --json`;
}

function buildVerifyCommand(format: string, out?: string): string {
  return `officegen verify ${out ?? `<repaired.${format}>`} --visual --json`;
}
